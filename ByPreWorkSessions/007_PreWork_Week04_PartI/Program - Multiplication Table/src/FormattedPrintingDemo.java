
public class FormattedPrintingDemo {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 20;
		System.out.printf("To display integer values %d and %d", num1, num2);
		System.out.println();
		
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.printf("Displaying boolean values %b and %b", b1, b2);
		System.out.println();
		
		float f1 = 2.5f;
		System.out.printf("Displaying float value %f", f1);
		System.out.println();
		
		String str = "Hello";
		System.out.printf("Displaying string value %s", str);
		System.out.println();
		
		
	}
}
